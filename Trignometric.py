
import math
n = float(input("Enter any number"))
print ("Number is:",n)

c = math.degrees(n)
print ("Angle in degree is:",c)

c = math.radians(n)
print ("Angle in radians is:",c)

c = math.acos(n)
print ("Arc Cosine is",c)

c = math.asin(n)
print ("Arc Sine is",c)

c = math.atan(n)
print ("Arc Tan is",c)

c = math.cos(n)
print ("Cosine is",c)

c = math.sin(n)
print ("Sine is",c)

c = math.tan(n)
print ("Tan is",c)
